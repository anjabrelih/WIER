  
class Page:
  
    def __init__(self, page_id, site_id, page_type_code, url, html_content, http_status_code, accessed_time, page_hash, crawl_delay, domain, disallow, last_accessed_time,page_type_code_raw, content_type, data_type, url_req):
        self.page_id = page_id
        self.site_id = site_id
        self.page_type_code = page_type_code
        self.url = url
        self.html_content = html_content
        self.http_status_code = http_status_code
        self.accessed_time = accessed_time
        self.last_accessed_time = last_accessed_time
        self.page_hash = page_hash
        self.crawl_delay = crawl_delay
        self.domain = domain
        self.disallow = disallow
        self.page_type_code_raw = page_type_code_raw
        self.content_type = content_type
        self.data_type = data_type
        self.url_req = url_req


    def __str__(self):
        return self.url


